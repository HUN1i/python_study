# list : 데이터를 묶어서 보관하는 묶음 자료형
# []를 사용해서 데이터를 묶는다

li = ["Python", 3.1, 5, True]
#        0       1   2    3 (=index)

print("li = ", li)

print("li[0] =", li[0])
print("li[2] =",li[2])

print("\nli의 타입 :", type(li))
#리스트는 자료형이 list
print("li[0]의 타입 :", type(li[0]))
print("li[3]의 타입 :", type(li[3]))
# 하지만 index를 하면 저장된 요소가 자료형이 된다
