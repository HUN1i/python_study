# 연산자 : 데이터를 가공하여 새로운 데이터를 얻는 기호들
# - 산술, 대입, 비교, 논리, 조건

# 산술 연산자 : 사칙연산을 수행
# 종류 : +, -, *, /, //, %, **

n=10

print(f"{n} + 10 = {n+10}")
print("n =", n)

# 산술연산은 그 자리에서 연산만 하고 변수에 영향을 주지 않는다

print(f"\n(n) - 6 = (n-6)")
print(f"(n) * 2 = (n * 2)")
print(f"(n) /4=(n/4)")
print(f"n//4={n//4}")
print(f"(n) % 4 = (n % 4)")
print(f"n ** 3 = (n ** 3\n")

