# 논리 연산자 : 참/거짓을 연산하는 연산자
# 종류 : and, or, not

# 1. and : 양쪽 모두 참이면 결과가 참
# - 다른 말로 곱(*) 연산이라고 한다
print("True AND True 결과 :", True and True)
print("True AND False 결과 :", True and False)
print("False AND True 결과 :", False and True)
print("False AND False 결과 :", False and False)

# 2. or : 한쪽이라도 참이면 결과가 참
# - 다른 말로 합(+) 연산이라고 한다
print("True or True 결과 :", True or True)
print("True or False 결과 :", True or False)
print("False or True 결과 :", False or True)
print("False or False 결과 :", False or False)


# 3. not : 참을 거짓으로, 거짓을 참으로 연산
# - 다른 말로 반전 연산이라고 한다
print("NOT True 결과 : ", not True)
print("NOT False 결과 :", not False)

