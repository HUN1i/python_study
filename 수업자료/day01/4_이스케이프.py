# 이스케이프(escape) : 탈출하다, 벗어나다

# 이스케이프 시퀀스 : 문자열 내에서 특수한 형태로 사용되는 문자들을 의미

print("Hello\nWorld")     # \n : new line, 줄바꿈

print("\nHello Python\n")   # 이스케이프는 몇 개든 사용 가능

print("여기는 \t\t 엄청 띄워집니다~")
# \t : tab, 들여쓰기

print("저희 Python 버전은 \"3.10\" 입니다~")
# \" : 문자열 묶는 기능을 제거

print('저희 공유폴더 주소는 \\\\221.164.9.51 입니다~')
# \\ : 이스케이프 기능을 제거